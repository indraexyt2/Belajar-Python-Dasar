# Aritmatika

a = 10
b = 5

# Pertambahan +
hasil = a + b
print(a,'+',b,'=',hasil)

# Pengurangan -
hasil = a - b
print(a,'-',b,'=',hasil)

# Perkalian *
hasil = a * b
print(a,'*',b,'=',hasil)

# Pembagian /
hasil = a / b
print(a,'/',b,'=',hasil)

# Eksponen (pangkat) **
hasil = a ** b
print(a,'**',b,'=',hasil)

# Modulus % --- Sisa pembagian, mis. 10 mod 3 = 1
hasil = a % b
print(a,'%',b,'=',hasil)

# Floor division // Pembagian yang dibulatkan
hasil = a // b
print(a,'//',b,'=',hasil)
